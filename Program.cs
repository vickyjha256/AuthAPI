using AuthAPI.Repositories;
using AuthAPI.Services;
using Microsoft.AspNetCore.Authentication.JwtBearer;
using Microsoft.EntityFrameworkCore;
using Microsoft.IdentityModel.Tokens;
using System.Text;

var builder = WebApplication.CreateBuilder(args);

// Add services to the container.

builder.Services.AddControllers();
// Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
builder.Services.AddEndpointsApiExplorer();
builder.Services.AddSwaggerGen();


builder.Services.AddDbContext<AppDbContext>(options => options.UseSqlServer(
                        builder.Configuration.GetConnectionString("AuthAPIConnectionString")
                        ));

builder.Services.AddScoped<IUserRepository, UserRepository>(); // User Repository is added.
builder.Services.AddScoped<IUserService, UserService>(); // User Service is added.

